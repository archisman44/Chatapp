function MessageInput({ onSend, onTyping, onStopTyping, typingUsers = [], username }) {
  const [text, setText] = useState('');
  const [showEmoji, setShowEmoji] = useState(false);
  const [activeCat, setActiveCat] = useState(0);
  const typingTimer = useRef(null);
  const isTyping = useRef(false);

  const handleChange = useCallback((e) => {
    setText(e.target.value);
    if (!isTyping.current) { isTyping.current = true; onTyping(); }
    clearTimeout(typingTimer.current);
    typingTimer.current = setTimeout(() => { isTyping.current = false; onStopTyping(); }, 1500);
  }, [onTyping, onStopTyping]);

  const handleSubmit = (e) => {
    e.preventDefault();
    if (text.trim()) {
      onSend(text.trim());
      setText('');
      clearTimeout(typingTimer.current);
      isTyping.current = false;
      onStopTyping();
      setShowEmoji(false);
    }
  };

  const others = typingUsers.filter((u) => u !== username);

  return (
    <div className="input-area">
      {others.length > 0 && (
        <div className="typing-indicator">
          <div className="typing-dots"><span /><span /><span /></div>
          <span className="typing-text">
            {others.length === 1 ? `${others[0]} is typing...` : `${others.join(', ')} are typing...`}
          </span>
        </div>
      )}
      {showEmoji && (
        <div className="emoji-picker">
          <div className="emoji-cat-tabs">
            {EMOJI_CATEGORIES.map((cat, i) => (
              <button
                key={i}
                className={`emoji-cat-btn${activeCat === i ? ' active' : ''}`}
                onClick={() => setActiveCat(i)}
                title={cat.title}
              >{cat.label}</button>
            ))}
          </div>
          <div className="emoji-cat-title">{EMOJI_CATEGORIES[activeCat].title}</div>
          <div className="emoji-grid">
            {EMOJI_CATEGORIES[activeCat].emojis.map((e) => (
              <span key={e} className="emoji-item" onClick={() => setText((p) => p + e)}>{e}</span>
            ))}
          </div>
        </div>
      )}
      <form className="message-input" onSubmit={handleSubmit}>
        <button
          type="button"
          className="emoji-toggle-btn"
          onClick={() => setShowEmoji((v) => !v)}
          title="Emoji"
        >😊</button>
        <input
          placeholder="Type a message..."
          value={text}
          onChange={handleChange}
          onKeyDown={(e) => { if (e.key === 'Escape') setShowEmoji(false); }}
        />
        <button type="submit">Send</button>
      </form>
    </div>
  );
}

export default MessageInput;
